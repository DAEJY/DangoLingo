/***********************************************************************
 * StudyDTO		    : 학습 진행 기록 DTO 클래스<br>
 * Inheritance	    : None
 ***********************************************************************/
public class StudyDTO
{
    // —————————————————————————————————————————————————————————————————————————————————————
    // 전역상수 관리 - 필수영역
    // —————————————————————————————————————————————————————————————————————————————————————

    // —————————————————————————————————————————————————————————————————————————————————————
    // 전역변수 관리 - 필수영역(정적변수)
    // —————————————————————————————————————————————————————————————————————————————————————

    // —————————————————————————————————————————————————————————————————————————————————————
    // 전역변수 관리 - 필수영역(인스턴스변수)
    // —————————————————————————————————————————————————————————————————————————————————————
    /** studyId      : 학습 기록 번호 */
    private int studyId;
    /** userId       : 유저 번호 */
    private int userId;
    /** wordsId      : 단어장 번호 */
    private int wordsId;
    /** japaneseId   : 마지막 본 일본어 번호 */
    private int japaneseId;
    /** studyDate    : 학습 일자 */
    private Date studyDate;
    /** studyCount   : 총 학습 단어 개수 */
    private int studyCount;

    // —————————————————————————————————————————————————————————————————————————————————————
    // 생성자 관리 - 필수영역(인스턴스함수)
    // —————————————————————————————————————————————————————————————————————————————————————
    /***********************************************************************
     * StudyDTO()       :  기본 생성자
     * @param void      : None
     ***********************************************************************/
    public StudyDTO()
    {
        try
        {
            // -----------------------------------------------------------------------------
            // 기타 초기화 작업 관리
            // -----------------------------------------------------------------------------
            ExceptionMgr.SetMode(ExceptionMgr.RUN_MODE.DEBUG);
            // -----------------------------------------------------------------------------
        }
        catch (Exception Ex)
        {
            ExceptionMgr.DisplayException(Ex);		// 예외처리(콘솔)
        }
    }
    // —————————————————————————————————————————————————————————————————————————————————————
    // 전역함수 관리 - 필수영역(정적함수)
    // —————————————————————————————————————————————————————————————————————————————————————

    // —————————————————————————————————————————————————————————————————————————————————————
    // 전역함수 관리 - 필수영역(인스턴스함수)
    // —————————————————————————————————————————————————————————————————————————————————————
    public int getStudyId() {
        return studyId;
    }

    public void setStudyId(int studyId) {
        this.studyId = studyId;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public int getWordsId() {
        return wordsId;
    }

    public void setWordsId(int wordsId) {
        this.wordsId = wordsId;
    }

    public int getJapaneseId() {
        return japaneseId;
    }

    public void setJapaneseId(int japaneseId) {
        this.japaneseId = japaneseId;
    }

    public Date getStudyDate() {
        return studyDate;
    }

    public void setStudyDate(Date studyDate) {
        this.studyDate = studyDate;
    }

    public int getStudyCount() {
        return studyCount;
    }

    public void setStudyCount(int studyCount) {
        this.studyCount = studyCount;
    }
    // —————————————————————————————————————————————————————————————————————————————————————
}
