    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public Date getStreakDate() {
        return streakDate;
    }

    public void setStreakDate(Date streakDate) {
        this.streakDate = streakDate;
    }

    public int getPoint() {
        return point;
    }

    public void setPoint(int point) {
        this.point = point;
    }
    // —————————————————————————————————————————————————————————————————————————————————————
